// 1. Рассчитать сумму элементов этого массива
// 2. Найти минимальное число
// 3. Найти есть ли в этом массиве число 3
console.log('Задание 3:');

const randomArr = [];
sum = 0;
min = 9;
for (let i = 0; i < 5; i++) {
    randomArr[i] = Math.floor(Math.random() * 9);
    sum += randomArr[i];
    min = randomArr[i] < min ? randomArr[i] : min;
}
console.log(randomArr);
console.log(`Сумма элементов маасива: ${sum}`);
console.log(`Минимальный элемент маасива: ${min}`);
